function icon(name, opts = {}) {
    const size = opts.size || 20;
    const cls = opts.class || 'ico';
    const paths = ICON_PATHS[name] || '';
    return `<svg class="${cls}" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="1.75" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${paths}</svg>`;
}

function txIcon(type, opts = {}) {
    const map = {
        transfer: 'send',
        exchange: 'exchange',
        invest: 'invest',
        alert: 'alert',
        document: 'document',
        card: 'card',
        mail: 'mail',
        shop: 'shop',
        store: 'store',
        plane: 'plane',
        building: 'building'
    };
    const name = map[type] || 'send';
    const size = opts.size || 20;
    return `<div class="tx-icon ${type}">${icon(name, { size })}</div>`;
}

function initDataIcons() {
    document.querySelectorAll('[data-icon]').forEach(el => {
        const size = parseInt(el.dataset.iconSize || '20', 10);
        el.innerHTML = icon(el.dataset.icon, { size, class: el.classList.contains('ico') ? 'ico' : 'ico ' + (el.dataset.iconClass || '') });
    });
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initDataIcons);
} else {
    initDataIcons();
}
